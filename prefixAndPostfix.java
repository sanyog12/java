import java.util.*;

public class Main
{
    public static void main(String[] args){
        int a  = 10;
        //postfix ++
        System.out.println(a++);
        System.out.println(a);
        //postfix --
        System.out.println(a--);
        System.out.println(a);
        //prefix ++
        System.out.println(++a);
        //prefix--
        System.out.println(--a);

    }
}
